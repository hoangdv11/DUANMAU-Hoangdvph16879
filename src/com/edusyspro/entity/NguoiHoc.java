package com.edusyspro.entity;

import java.util.Date;

/**
 *
 * @author ADMIN
 */
public class NguoiHoc {

    private String MaNH, HoTen;
    private Date NgaySinh;
    private boolean GioiTinh;
    private String Email, DienThoai;
    private Date ngayDK;
    private String MaNV;
    private String GhiChu;

    public NguoiHoc() {
    }

    public NguoiHoc(String MaNH, String HoTen, Date NgaySinh, boolean GioiTinh, String Email, String DienThoai, Date ngayDK, String MaNV, String GhiChu) {
        this.MaNH = MaNH;
        this.HoTen = HoTen;
        this.NgaySinh = NgaySinh;
        this.GioiTinh = GioiTinh;
        this.Email = Email;
        this.DienThoai = DienThoai;
        this.ngayDK = ngayDK;
        this.MaNV = MaNV;
        this.GhiChu = GhiChu;
    }

    public String getMaNH() {
        return MaNH;
    }

    public void setMaNH(String MaNH) {
        this.MaNH = MaNH;
    }

    public String getHoTen() {
        return HoTen;
    }

    public void setHoTen(String HoTen) {
        this.HoTen = HoTen;
    }

    public Date getNgaySinh() {
        return NgaySinh;
    }

    public void setNgaySinh(Date NgaySinh) {
        this.NgaySinh = NgaySinh;
    }

    public boolean isGioiTinh() {
        return GioiTinh;
    }

    public void setGioiTinh(boolean GioiTinh) {
        this.GioiTinh = GioiTinh;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getDienThoai() {
        return DienThoai;
    }

    public void setDienThoai(String DienThoai) {
        this.DienThoai = DienThoai;
    }

    public Date getNgayDK() {
        return ngayDK;
    }

    public void setNgayDK(Date ngayDK) {
        this.ngayDK = ngayDK;
    }

    public String getMaNV() {
        return MaNV;
    }

    public void setMaNV(String MaNV) {
        this.MaNV = MaNV;
    }

    public String getGhiChu() {
        return GhiChu;
    }

    public void setGhiChu(String GhiChu) {
        this.GhiChu = GhiChu;
    }

}
